// Generate sector data for funds that don't have specific data
export const getSectorDataForFund = (fundId: string): FundSectorData | null => {
  const existing = mockSectorData.find(s => s.fundId === fundId);
  if (existing) return existing;

  const fund = mockFunds.find(f => f.id === fundId);
  if (!fund) return null;

  // Generate generic sector allocation based on category
  if (fund.category === 'Debt' || fund.category === 'Liquid') {
    return {
      fundId,
      fundName: fund.name,
      sectors: [
        { sector: 'Government Securities', percentage: 35, color: SECTOR_COLORS[0] },
        { sector: 'Corporate Bonds', percentage: 28, color: SECTOR_COLORS[1] },
        { sector: 'AAA Rated', percentage: 22, color: SECTOR_COLORS[2] },
        { sector: 'AA Rated', percentage: 10, color: SECTOR_COLORS[3] },
        { sector: 'Others', percentage: 5, color: SECTOR_COLORS[4] },
      ],
    };
  }

  if (fund.category === 'Hybrid') {
    return {
      fundId,
      fundName: fund.name,
      sectors: [
        { sector: 'Equity', percentage: 65, color: SECTOR_COLORS[0] },
        { sector: 'Debt', percentage: 30, color: SECTOR_COLORS[1] },
        { sector: 'Cash', percentage: 5, color: SECTOR_COLORS[2] },
      ],
    };
  }

  if (fund.category === 'Index') {
    return {
      fundId,
      fundName: fund.name,
      sectors: [
        { sector: 'Financial Services', percentage: 38, color: SECTOR_COLORS[0] },
        { sector: 'IT', percentage: 14, color: SECTOR_COLORS[1] },
        { sector: 'Oil & Gas', percentage: 12, color: SECTOR_COLORS[5] },
        { sector: 'Consumer Goods', percentage: 11, color: SECTOR_COLORS[2] },
        { sector: 'Automobile', percentage: 8, color: SECTOR_COLORS[4] },
        { sector: 'Others', percentage: 17, color: SECTOR_COLORS[6] },
      ],
    };
  }

  return null;
};
